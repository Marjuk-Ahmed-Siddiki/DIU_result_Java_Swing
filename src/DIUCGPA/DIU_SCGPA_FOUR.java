    private void completecreditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completecreditActionPerformed
        // TODO add your handling code here:

        int one_credit = Integer.parseInt(credit_1.getText());
        int two_credit = Integer.parseInt(credit_2.getText());
        int three_credit = Integer.parseInt(credit_3.getText());
        int four_credit = Integer.parseInt(credit_4.getText());
        

        int total_credit = one_credit + two_credit + three_credit + four_credit;

        totalcredit.setText(""+total_credit);

        float one_cgpa = Float.parseFloat(cgpa_1.getText());
        float two_cgpa = Float.parseFloat(cgpa_2.getText());
        float three_cgpa = Float.parseFloat(cgpa_3.getText());
        float four_cgpa = Float.parseFloat(cgpa_4.getText());
        

        float total_cgpa = (one_cgpa * one_credit) + (two_cgpa * two_credit) + (three_cgpa * three_credit) + (four_cgpa * four_credit);

        float total_scgpa = total_cgpa/total_credit;

        scgpa.setText(""+total_scgpa);

        String gradee = null,rem = null;

        if(total_scgpa==4.00){
            gradee = "A+";
            rem = "Outstanding";
        }else if(total_scgpa>=3.75){
            gradee = "A";
            rem = "Excellent";
        }else if(total_scgpa>=3.50){
            gradee = "A-";
            rem = "Very Good";
        }else if(total_scgpa>=3.25){
            gradee = "B+";
            rem = "Good";
        }else if(total_scgpa>=3.00){
            gradee = "B";
            rem = "Satisfactory";
        }else if(total_scgpa>=2.75){
            gradee = "B-";
            rem = "Above Average";
        }else if(total_scgpa>=2.50){
            gradee = "C+";
            rem = "Average";
        }else if(total_scgpa>=2.25){
            gradee = "C";
            rem = "Bellow Average";
        }else if(total_scgpa>=2.00){
            gradee = "D";
            rem = "Pass";
        }else if(total_scgpa>=0.00){
            gradee = "F";
            rem = "fail";
        }

        grade.setText(""+gradee);
        remark.setText(""+rem);

    }//GEN-LAST:event_completecreditActionPerformed

